package _06_class_._02_class_coposition;

public class ClassComposition {
/*
	 필드
	 - 객체의 데이터를 저장하는 역활
	 - 필드 초기값을 설정하지 않으면 기본 값으로 초기화
*/
	int i; // 기본값 0
	double d; // 기본값 0.0
	boolean b; // 기본값 false
	String s; // 기본값 null

/*
	생성자
	- 객체를 생성할 때 객체의 초기화 역활
	- new 생성자로 객체를 생성할 때 실행
	- 클래스에 생성자 선언이 없으면 컴파일러가 기본 생성자를 자동으로 추가
*/
	public ClassComposition() {}
	
/*
  	매소드
  	- 객체가 수행하는 동작
*/
	public void method() {}
}
